    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        FileReader file = null;
        try {  
            file = new FileReader("Comment.txt");// Creates a FileReader                       
            BufferedReader rdfile = new BufferedReader(file); // Creates a BufferedReader
        
            String userComment = jTextField1.getText();//get input 
            String UserName = jTextField2.getText();//get input 
            
            
            String[] applicantline = new String[100]; // volume           
            int x = 0;
                while ((applicantline[x] = rdfile.readLine()) != null) {
                    x++;
                }
                rdfile.close();
                
        
            applicantline[x] = UserName + "\t" + userComment;

            PrintWriter wrfile = new PrintWriter(new FileWriter("Comment.txt"));

            for (int j = 0; applicantline[j] != null; j++) 
            wrfile.println(applicantline[j]);


            wrfile.close();

            JOptionPane.showMessageDialog(null, "Succesfully Add Comment!", " Ok!  :-)", JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException e) {
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Some input may be invalid!", "Warning!", JOptionPane.WARNING_MESSAGE);
        } catch (NullPointerException e){
            JOptionPane.showMessageDialog(null, "Enter item!", "Oops Wait...!", JOptionPane.ERROR_MESSAGE);
        } 

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        File file = new File("Comment.txt");
        
        try {
            BufferedReader br = new BufferedReader(new FileReader(file));
            DefaultTableModel model= (DefaultTableModel)jTable1.getModel();
            model.setRowCount(0);
            // get the first line
            // get the columns name from the first line and show them as column name on table
            String firstLine = br.readLine().trim();
            String[] columnsName = firstLine.split(",");
            model.setColumnIdentifiers(columnsName);
            
            // get lines from txt file
            Object[] tableLines = br.lines().toArray();
            
            // extratct data from lines
            // set data to jtable model
            for(int i = 0; i < tableLines.length; i++)
            {
                String line = tableLines[i].toString().trim();
                String[] dataRow = line.split("\t");
                model.addRow(dataRow);
            }
            
            
        } catch (IOException ex) {
          
        }
    }//GEN-LAST:event_jButton2ActionPerformed

