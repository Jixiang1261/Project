<p style="font-family:Fantasy;font-size:30px;">Adjectives</p>
<p>We can use adjectives to describe people, places and things.</p>

<p style="font-family:Comic Sans MS"><i>We've got a <b>small</b> car.</i></p>
<p style="font-family:Comic Sans MS"><i>I saw a <b>white</b> bird.</i></p>
<p style="font-family:Comic Sans MS"><i>This book isn't very <b>old</b>.</i></p>

<p style="font-family:Courier;color:red;font-size:20px;">How to use them</p>
<p>We don't add s to the adjective when it's plural.</p>

<p style="font-family:Comic Sans MS"><i>My brothers are <b>short</b>.</i></p>
<p style="font-family:Comic Sans MS"><i>We've got three <b>black</b> cats.</i></p>
<p style="font-family:Comic Sans MS"><i>She watched some <b>old</b> films.</i></p>

<p>Put size adjectives before colour adjectives.</p>

<p style="font-family:Comic Sans MS"><i>I've got a <b>big</b>, <b>blue</b> ball.</i></p>
<p style="font-family:Comic Sans MS"><i>That building is <b>tall</b> and <b> grey</b></i></p>
