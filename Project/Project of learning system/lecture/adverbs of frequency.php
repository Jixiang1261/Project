<p style="font-family:Fantasy;font-size:30px;">Adverbs of frequency</p>
<p>We can use adverbs of frequency to say how often we do something.</p>

<p style="font-family:Comic Sans MS"><i>I <b>always</b> go to the cinema at the weekend.</i></p>
<p style="font-family:Comic Sans MS"><i>She <b>usually</b> has cereal for breakfast.</i></p>
<p style="font-family:Comic Sans MS"><i>They <b>sometimes</b> watch TV in the evenings.</i></p>
<p style="font-family:Comic Sans MS"><i>We <b>never</b> go to museums.</i></p>

<p style="font-family:Courier;color:red;font-size:20px;">How to use them</p>
<p>Always means every time. Usually means most times. Sometimes means a small number of times. Never means no times.</p>

<p style="font-family:Comic Sans MS"><i>She <b>always</b> practises the piano in the evenings. Every day!</i></p>
<p style="font-family:Comic Sans MS"><i>We <b>usually</b> go to the park on Sundays. Most Sundays!</i></p>
<p style="font-family:Comic Sans MS"><i>They <b>sometimes</b> make a cake. Just for special days!</i></p>
<p style="font-family:Comic Sans MS"><i>He <b>never</b> forgets his homework. No times!</i></p>

<p>We usually put the adverb before the verb.</p>

<p style="font-family:Comic Sans MS"><i>She <b>always does</b> her homework after school.</i></p>
<p style="font-family:Comic Sans MS"><i>I <b>usually have</b> cereal for breakfast.</i></p>

<p>If the verb is to be, we put the adverb after the verb.</p>

<p style="font-family:Comic Sans MS"><i>They <b>are usually</b> on holiday in July.</i></p>
<p style="font-family:Comic Sans MS"><i>He <b>is sometimes</b> late for school.</i></p>

<p>To ask a question, use often or how often.</p>

<p style="font-family:Comic Sans MS"><i>Does she play football <b>often</b>?</i></p>
<p style="font-family:Comic Sans MS"><i><b>How often</b> do you play computer games?</i></p>
