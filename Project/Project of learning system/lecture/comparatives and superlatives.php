<p style="font-family:Fantasy;font-size:30px;">Comparatives and superlatives</p>
<p>We can use comparatives to compare things and superlatives to say which thing is top in a group.</p>

<p style="font-family:Comic Sans MS"><i>Cats are <b>faster</b> than mice.</i></p>
<p style="font-family:Comic Sans MS"><i>Science is <b>more difficult</b> than maths.</i></p>
<p style="font-family:Comic Sans MS"><i>The cheetah is <b>the fastest</b> animal.</i></p>
<p style="font-family:Comic Sans MS"><i>I think art is <b>the most difficult</b> subject.</i></p>

<p style="font-family:Courier;color:red;font-size:20px;">How to use them</p>
<p>For adjectives with one syllable, add er and est. Sometimes the spelling changes. Don't forget the for the superlative.</p>

<p style="font-family:Comic Sans MS"><i>tall - taller - the tallest</i></p>
<p style="font-family:Comic Sans MS"><i>I'm <b>taller</b> than my brother. My father is <b>the tallest</b> in my family.</i></p>
<p style="font-family:Comic Sans MS"><i>big - bigger - the biggest</i></p>
<p style="font-family:Comic Sans MS"><i>My room is <b>bigger</b> than my sister's room. Our parents' room is <b>the biggest</b> room in the house.</i></p>

<p>For adjectives with two syllables which end in y, take away the y and add ier and iest. Don't forget the for the superlative.</p>

<p style="font-family:Comic Sans MS"><i>easy - easier - the easiest</i></p>
<p style="font-family:Comic Sans MS"><i>Maths is <b>easier</b> than science. English is <b>the easiest</b> subject.</i></p>

<p>For other adjectives with two or more syllables, use more and most. Don't forget the for the superlative.</p>

<p style="font-family:Comic Sans MS"><i>interesting - more interesting - the most interesting</i></p>
<p style="font-family:Comic Sans MS"><i>That book is <b>more interesting</b> than my homework. This TV programme is <b>the most interesting</b>!</i></p>

<p>The adjectives good, bad and far have special forms.</p>

<p style="font-family:Comic Sans MS"><i>good - better - the best</i></p>
<p style="font-family:Comic Sans MS"><i>bad - worse - the worst</i></p>
<p style="font-family:Comic Sans MS"><i>far - further - the furthest</i></p>
